/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  // One of the boards malfunction so the MCU 3.3V should be started as soon as
  // possible. This work-around requires do be done before clock and other
  // initialization.
  LL_AHB2_GRP1_EnableClock(LL_AHB2_GRP1_PERIPH_GPIOB);
  LL_GPIO_SetOutputPin(DO_DC_DC_EN_GPIO_Port, DO_DC_DC_EN_Pin);
  LL_GPIO_InitTypeDef GPIO_InitStruct = {0};
  GPIO_InitStruct.Pin = DO_DC_DC_EN_Pin;
  GPIO_InitStruct.Mode = LL_GPIO_MODE_OUTPUT;
  GPIO_InitStruct.Speed = LL_GPIO_SPEED_FREQ_LOW;
  GPIO_InitStruct.OutputType = LL_GPIO_OUTPUT_PUSHPULL;
  GPIO_InitStruct.Pull = LL_GPIO_PULL_NO;
  LL_GPIO_Init(GPIOB, &GPIO_InitStruct);    
  
  SCB->VTOR = APP_PROGRAM_FLASH_START;
  HalWdtFeed();
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  // STM32CubeMX V6 HAL drivers for STM32L4 has rewritten the definition for HSI
  // default calibration and it is now wrong. An offset of 15% on the system 
  // clock has been observed.
  // As a work-around the HSI default calibration is undefined and defined again
  // with an working implementation copied from STM32CubeMX V5 HAL drivers.
  
#undef RCC_HSICALIBRATION_DEFAULT  
  
#if defined(STM32L431xx) || defined(STM32L432xx) || defined(STM32L433xx) || \
    defined(STM32L442xx) || defined(STM32L443xx) || defined(STM32L471xx) || \
    defined(STM32L475xx) || defined(STM32L476xx) || defined(STM32L485xx) || \
    defined(STM32L486xx)
#define RCC_HSICALIBRATION_DEFAULT     0x10U
#else
#define RCC_HSICALIBRATION_DEFAULT     0x40U
#endif    
  
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_RTC_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
  MX_SPI1_Init();
  MX_TIM2_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  HalWdtFeed();
  HalInterruptEnable(NULL);
  EvosInit();
  
	SIF_Init(true); 
  HalInit();
	PlfInit();	
	SIF_Init(false); 
  
  // Application init handles SUP_Init and SHC_Init
  APP_Init();
  CIF_Init();  // Last called befor Evos Start
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  EvosSchedulerStart(NULL);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  /* USER CODE END 3 */
}

